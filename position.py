class Position():
	def __init__(self, x, y, h, confidence):
		self.x = x
		self.y = y
		self.h = h,
		self.confidence = confidence
 
	def update(new_vals):
		if(new_vals[0]):
			self.x = new_vals[0]
		if(new_vals[1]):
			self.y = new_vals[1]
		if(new_vals[2]):
			self.h = new_vals[2]
		if(new_vals[3]):
			self.confidence = new_vals[3]



